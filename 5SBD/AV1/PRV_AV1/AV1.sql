CREATE TABLE CLIENTES_CLI (
    ID_CLIENTE INT IDENTITY PRIMARY KEY,
    NOME VARCHAR(150),
    EMAIL VARCHAR(150),
    CPF VARCHAR(14),
    TELEFONE VARCHAR(20)
);

CREATE TABLE PRODUTOS_PRD (
    ID_PRODUTO INT IDENTITY PRIMARY KEY,
    SKU VARCHAR(50),
    UPC VARCHAR(50),
    NOME_PRODUTO VARCHAR(200),
    QTD_ESTOQUE INT DEFAULT 0
);

CREATE TABLE PEDIDOS_PED (
    ID_PEDIDO VARCHAR(50) PRIMARY KEY,
    DATA_PEDIDO DATETIME,
    ID_CLIENTE INT,
    VL_TOTAL DECIMAL(10,2),
    STATUS VARCHAR(20),

    FOREIGN KEY (ID_CLIENTE)
        REFERENCES CLIENTES_CLI(ID_CLIENTE)
);

CREATE TABLE ITPEDIDO_ITP (
    ID_ITEM INT IDENTITY PRIMARY KEY,
    ID_PEDIDO VARCHAR(50),
    ID_PRODUTO INT,
    QTD INT,
    VL_UNIT DECIMAL(10,2),

    FOREIGN KEY (ID_PEDIDO)
        REFERENCES PEDIDOS_PED(ID_PEDIDO),

    FOREIGN KEY (ID_PRODUTO)
        REFERENCES PRODUTOS_PRD(ID_PRODUTO)
);

CREATE TABLE MOVIMENTO_MOV (
    ID_MOVIMENTO INT IDENTITY PRIMARY KEY,
    ID_PEDIDO VARCHAR(50),
    ID_PRODUTO INT,
    QTD_MOVIMENTADA INT,
    ESTOQUE_ANTES INT,
    ESTOQUE_DEPOIS INT,
    DATA_MOVIMENTO DATETIME DEFAULT GETDATE()
);

CREATE TABLE COMPRAS_COM (
    ID_COMPRA INT IDENTITY PRIMARY KEY,
	ID_PEDIDO VARCHAR(50),
    ID_PRODUTO INT,
    QTD_SOLICITADA INT,
    DATA_SOLICITACAO DATETIME DEFAULT GETDATE(),
	
	FOREIGN KEY (ID_PEDIDO) REFERENCES PEDIDOS_PED(ID_PEDIDO),
    FOREIGN KEY (ID_PRODUTO) REFERENCES PRODUTOS_PRD(ID_PRODUTO)
);

