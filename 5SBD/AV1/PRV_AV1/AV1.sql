CREATE OR ALTER PROCEDURE PRC_AV1
AS
BEGIN

DECLARE
    @V_IDPEDIDO VARCHAR(50),
    @V_IDPRD INT,
    @V_QTD INT,
    @V_ESTOQUE INT,
    @V_ATENDER INT;

DECLARE C_PEDIDOS CURSOR FOR
    SELECT ID_PEDIDO
    FROM PEDIDOS_PED
    WHERE STATUS IS NULL
    ORDER BY VL_TOTAL DESC, DATA_PEDIDO ASC;

OPEN C_PEDIDOS;
FETCH NEXT FROM C_PEDIDOS INTO @V_IDPEDIDO;

WHILE @@FETCH_STATUS = 0
BEGIN
    SET @V_ATENDER = 1;

    DECLARE C_ITENS CURSOR FOR
        SELECT ID_PRODUTO, QTD
        FROM ITPEDIDO_ITP
        WHERE ID_PEDIDO = @V_IDPEDIDO;

    OPEN C_ITENS;
    FETCH NEXT FROM C_ITENS INTO @V_IDPRD, @V_QTD;

    WHILE @@FETCH_STATUS = 0
    BEGIN
        SELECT @V_ESTOQUE = QTD_ESTOQUE
        FROM PRODUTOS_PRD
        WHERE ID_PRODUTO = @V_IDPRD;

        IF @V_ESTOQUE < @V_QTD
        BEGIN
            SET @V_ATENDER = 0;

            INSERT INTO COMPRAS_COM(
				ID_PEDIDO,
                ID_PRODUTO,
                QTD_SOLICITADA
            )
            VALUES (
				@V_IDPEDIDO,
                @V_IDPRD,
                @V_QTD
            );
        END

        FETCH NEXT FROM C_ITENS INTO @V_IDPRD, @V_QTD;
    END

    CLOSE C_ITENS;
    DEALLOCATE C_ITENS;

IF @V_ATENDER = 1
BEGIN

    INSERT INTO MOVIMENTO_MOV (
        ID_PEDIDO,
        ID_PRODUTO,
        QTD_MOVIMENTADA,
        ESTOQUE_ANTES,
        ESTOQUE_DEPOIS
    )
    SELECT
        i.ID_PEDIDO,
        i.ID_PRODUTO,
        i.QTD,
        p.QTD_ESTOQUE,
        p.QTD_ESTOQUE - i.QTD
    FROM ITPEDIDO_ITP i
    INNER JOIN PRODUTOS_PRD p
        ON p.ID_PRODUTO = i.ID_PRODUTO
    WHERE i.ID_PEDIDO = @V_IDPEDIDO;

    UPDATE p
    SET p.QTD_ESTOQUE = p.QTD_ESTOQUE - i.QTD
    FROM PRODUTOS_PRD p
    INNER JOIN ITPEDIDO_ITP i
        ON p.ID_PRODUTO = i.ID_PRODUTO
    WHERE i.ID_PEDIDO = @V_IDPEDIDO;

    UPDATE PEDIDOS_PED
    SET STATUS = 'ATENDIDO'
    WHERE ID_PEDIDO = @V_IDPEDIDO;

END
    ELSE
    BEGIN
        UPDATE PEDIDOS_PED
        SET STATUS = 'PENDENTE'
        WHERE ID_PEDIDO = @V_IDPEDIDO;
    END

    FETCH NEXT FROM C_PEDIDOS INTO @V_IDPEDIDO;
END

CLOSE C_PEDIDOS;
DEALLOCATE C_PEDIDOS;
END;
GO

EXEC PRC_AV1;

